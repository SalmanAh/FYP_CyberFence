// Generate deterministic wallet addresses
const generateWallet = (seed: number) => {
  const hex = '0123456789abcdef';
  let addr = '0x';
  for (let i = 0; i < 40; i++) {
    addr += hex[(seed * 7 + i * 13) % 16];
  }
  return addr;
};

